# в переменную "num" присваевается значение 0
num = 0
# создается цикл "while" которому дается условие если переменная "num" меньше чем 1000 то
while num < 1000:
    # добавить к переменной "num" еденицу и вывести на экран
    num = num + 1
    print(num)
# цикл будет продолжатся пока число переменной "num" не достигнет числа заданной в условии цикла